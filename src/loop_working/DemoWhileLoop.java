package loop_working;

public class DemoWhileLoop {

    public static void main(String[] args) {

        int i=0;

        while(i <= 5){

            System.out.println("Hello, This is while Loop..."+i);
            i++;

        }

        int j=10;

        while(j>0){
            System.out.println("Hello, This is decrement while Loop..."+j);
            j--;
        }


    }
}
