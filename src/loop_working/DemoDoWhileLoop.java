package loop_working;

public class DemoDoWhileLoop {

    public static void main(String[] args) {

        int i=0;

        do{
            System.out.println("Hello, This is Do while loop ..."+i);
            i++;
        } while (i < 5);

        int j=10;
        do{
            System.out.println("Hello, This is Do while loop ..."+j);
            j--;
        } while (j >0);


    }
}
