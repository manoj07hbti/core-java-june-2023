package loop_working;

public class Demoloopclass {
    public static void main(String[] args) {

        for(int i=1; i<=10; i++){
            System.out.println("Hello inside the LOOP"+i);
        }

        for (int i=10; i>=0; i--){
            System.out.println("Decrement for loop"+i);
        }
    }


}
