package loop_working;

public class InfiniteLoop {
    public static void main(String[] args) {

                for(int i=1; i>0; i++){

                    System.out.println("Infinite loop :"+i);
                }

                for(int i=-1; i<0; i--){

                    System.out.println("Infinite loop :"+i);
                }

    }
}
