package loops;

public class TableOfFive {
    public static void main(String[] args) {
       int number = 5;
        for(int i=1; i<=10; i++){
            System.out.println(number+"X" +i+" = "+number*i);
        }


        int number1 = 55;
        for(int i=1; i<=10; i++){
            System.out.println(number1+"X" +i+" = "+number1*i);
        }



    }
}
