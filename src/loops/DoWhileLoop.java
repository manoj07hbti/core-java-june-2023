package loops;

public class DoWhileLoop {

    public static void main(String[] args) {
         int i=0;
         do {
             System.out.println("this is me :"+i);
             i++;
         }while (i<5);


        /*===========================================================*/

        int j=10;
        do {
            System.out.println("this is not me :"+j);
            j--;
        }while (j>5);


    }
}
