package class_loops_forloops;

public class For_loops_class {

    public static void main(String[] args) {


        int number = 5;
        for (int i=1; i<=10; i++){

            System.out.println(number+"X" +i+ "=" +number*i);
        }
        number = 10;
        for (int i=1; i<=10; i++){

            System.out.println(number+"X" +i+ "=" +number*i);
        }
number=4;

        for (int i=1; i<=10; i++){

            System.out.println(number +"X"+i+"="+ number*i);
        }



    }


}



