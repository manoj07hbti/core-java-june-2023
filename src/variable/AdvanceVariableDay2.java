package variable;

public class AdvanceVariableDay2 {
    public static void main(String[] args) {
        int a=4;
        int b=9;
        int sum=a+b;
        System.out.println("Addition of a and b :" +sum);

        int sub=a-b;
        System.out.println("Printing Subtraction :" +sub);

        int multi=a*b;
        System.out.println("Printing Multipication :" +multi);

        int divide=a/b;
        System.out.println("Printing Division :" +divide);

        int i=2;
        i++;
        System.out.println("Printing after increament operator :"+i);

        i--;
        System.out.println("Printing Decreament value of i is : "+i);

        int y=8;
        int z=9;

        y+=z;
        System.out.println("Printing y :"+y);

        y-=z;
        System.out.println("Printing y :"+y);


    }


}
