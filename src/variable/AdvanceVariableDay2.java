package variable;

public class AdvanceVariableDay2 {

    public static void main(String[] args) {

        int a=3;
        int b=9;

        int sum=a+b; // we have added data of a and b

        System.out.println("Addition of a and b is :"+sum);

        int sub=a-b;

        System.out.println("Subtraction is : "+sub);

        int multi= a*b;

        System.out.println("Multi is : "+multi);


        double divide=(double) a/b; //

        System.out.println("Division of a and b is : "+divide);

        int i=5;
        i++; // increment operator i=i+1

        System.out.println("Printing is after increment operator "+i);

        i--; // decrement operator i=i-1

        System.out.println("Decrement value of i is : "+i);

        int x=4;
        int y=7;

        x+=y; // x=x+y ;

        System.out.println("Printing X: "+x);

        x-=y; //x=x-y

        System.out.println(" Printing x after minus : "+x);


    }
}
