package variable;

public class DemoVariables {

    public static void main(String[] args) {

       //  SYNTAX : Datatype varName;  variable declaration

        String name;  // we have declared a variable name which will store String data
        // SYNTAX: varName=value;
        name="Jatin" ;  // assigned Jatin value to name variable, String value will always be in double quot " "

        System.out.println("Printing variable : " +name);

        int age; // we have declared a variable age which will store integer data
        age=21;  // assigned 21 value to age variable
        System.out.println("Printing age :"+age);

        double price; // we have declared price variable to store decimal data
        price=234.45; // assigned 234.45 value to price variable
        System.out.println("Printing price: "+price);

        String cityName;
        cityName="Agra";
        System.out.println("Printing cityName: "+cityName);

        double salary;
        salary=35000.75;
        System.out.println("Printing salary: "+salary);

        int ram;
        ram=8;
        System.out.println("Printing ram: " +ram);


        System.out.println("This is example for latest code in git..");


        double salary1;
        salary1=35000.75;
        System.out.println("Printing salary: "+salary1);

        int ram1;
        ram1=8;
        System.out.println("Printing ram: " +ram1);

        int ram2=8;
        System.out.println("this is ram 2: -"+ ram2);


    }



}
