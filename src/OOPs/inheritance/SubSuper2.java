package OOPs.inheritance;

public class SubSuper2 extends SubSuper {
    public static void main(String[] args) {
        SubSuper2 obj = new SubSuper2();
        obj.Add(23, 43);
        obj.Sub(23, 5);
        obj.mul(34, 5);
        obj.div(50, 5);
        obj.Display("Multi-Level inheritance Class");
        obj.Remainder(23, 5);
        obj.division(23, 5);

    }
}
