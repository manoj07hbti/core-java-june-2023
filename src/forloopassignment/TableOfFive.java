package forloopassignment;

public class TableOfFive {


    public static void main(String[] args) {

        int number=5;

        /*SYNTAX FOR INCREMENT FOR LOOP
            for(start_condition; expression; increment )
         */

        for (int i=1; i<=10; i++)
        {
            System.out.println(number +"*"+i +"="+number*i);

        }
    }
}
