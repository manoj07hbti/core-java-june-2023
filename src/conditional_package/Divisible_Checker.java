package conditional_package;

public class Divisible_Checker {
    public static void main(String[] args) {

        int number = 21;
        if (number % 3 == 0) {
            System.out.println(number + " is divisible by 3.... ");
        } else {
            System.out.println(number + " is not divisible by 3.... ");

        }

    }

}
