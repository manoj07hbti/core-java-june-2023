package Loops;

public class While_loops {
    public static void main(String[] args) {

    int i=1;
    while (i<=15){
        System.out.println("Printing  while loops..... "+i);
        i++;
    }
    int j=10;
    while (j>0){
        System.out.println("Printing Decrement while loops....... "+j);
        j--;
    }
}
 }