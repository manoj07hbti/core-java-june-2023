package PatternStatement;

public class PatternSecondProblem {
    public static void main(String[] args) {
        int a=7; // number of rows:
        int b=6; // number of colum:
        for (int i=1;i<=a;i++){
            for (int j=1;j<=b;j++){
                System.out.print(" *  ");
            }
            System.out.println();
        }
    }
}
