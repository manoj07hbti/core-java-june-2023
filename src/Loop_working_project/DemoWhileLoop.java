package Loop_working_project;

public class DemoWhileLoop {
    public static void main(String[] args) {

        int number = 5;
        for (int i = 1; i <= 10; i++) {
            System.out.println(number + "x" + i + " = " + number * i);

        }
        int numbers = 10;
        for (int i = 1; i <= 10; i++) {

            System.out.println(numbers + "X" + i + " = " + numbers * i);

        }
    }
}
